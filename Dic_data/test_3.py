# Đoạn văn bản đầu vào
text = "Tra tấn bằng cách treo ngược các đệ tử trên trần nhà đã làm tay các đệ tử bị liệt vĩnh viển tại trại cưỡng bức lao động của phụ nữ Huhhot, khu Nội Mông cổ"

# Tách đoạn văn bản thành các từ (sử dụng khoảng trắng để tách)
words = text.split()

# Đếm số từ
word_count = len(words)

# In ra kết quả
print(f"Số từ trong đoạn văn bản là: {word_count}")
